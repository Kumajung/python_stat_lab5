""" Hypothesis Testing หน้า 58
มูลนิธิการศึกษาการฟื้นฟูสมรรถภาพทางการแพทย์รายงานว่าค่าใช้จ่ายเฉลี่ย
ในการฟื้นฟูสมรรถภาพสําหรับผู้ที่ตกเป็นเหยื่อโรคหลอดเลือดสมองอยู่ที่ 24,672 ดอลลาร์ 
เพื่อดูว่าค่าใช้จ่ายเฉลี่ยในการฟื้นฟูสมรรถภาพในโรงพยาบาลขนาดใหญ่
แตกต่างกันหรือไม่นักวิจัยได้สุ่มกลุ่มตัวอย่างของผู้ประสบโรคหลอดเลือดสมอง 35 คน
และพบว่าค่าใช้จ่ายเฉลี่ยในการฟื้นฟูสมรรถภาพอยู่ที่ 25,226 ดอลลาร์ 
ค่าเบี่ยงเบนมาตรฐานของประชากรคือ 3,251 ดอลลาร์ 
ที่ α = 0.01 สรุปได้หรือไม่ว่าค่าใช้จ่ายเฉลี่ย
ของโรงพยาบาลขนาดใหญ่แตกต่างจาก 24,672 ดอลลาร์
"""
import math
from scipy.stats import norm

# ข้อมูลที่กำหนด
mu = 24672  # ค่าเฉลี่ยของประชากร
x_bar = 25226  # ค่าเฉลี่ยของตัวอย่าง
sigma = 3251  # ค่าเบี่ยงเบนมาตรฐานของประชากร
n = 35  # ขนาดตัวอย่าง
alpha = 0.01  # ระดับนัยสำคัญ

# คำนวณ z-score
z = (x_bar - mu) / (sigma / math.sqrt(n))

# คำนวณ p-value (two-tailed test)
p_value = 2 * (1 - norm.cdf(abs(z)))

# แสดงผลลัพธ์
print(f"z-score: {z:.4f}")
print(f"p-value: {p_value:.4f}")

# ตัดสินใจ
if p_value < alpha:
    print("ปฏิเสธสมมติฐานว่าง (Null Hypothesis) ค่าเฉลี่ยของค่าใช้จ่ายการฟื้นฟูที่โรงพยาบาลใหญ่แตกต่างจาก $24,672")
else:
    print("ไม่สามารถปฏิเสธสมมติฐานว่าง (Null Hypothesis) ค่าเฉลี่ยของค่าใช้จ่ายการฟื้นฟูที่โรงพยาบาลใหญ่ไม่แตกต่างจาก $24,672")
